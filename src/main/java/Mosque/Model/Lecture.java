package Mosque.Model;

public class Lecture  extends Activity{

	String LectureSlot;
	String UstazName;
	String ActivityID;
	

	public String getLectureSlot() {
		return LectureSlot;
	}
	public void setLectureSlot(String lectureSlot) {
		LectureSlot = lectureSlot;
	}
	public String getUstazName() {
		return UstazName;
	}
	public void setUstazName(String ustazName) {
		UstazName = ustazName;
	}
	public String getActivityID() {
		return ActivityID;
	}
	public void setActivityID(String activityID) {
		ActivityID = activityID;
	}

}
